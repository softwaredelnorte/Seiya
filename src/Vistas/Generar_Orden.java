    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        // TODO add your handling code here:
        if(this.orden.getText().equals("")){
            this.mensaje.setText("Complete los Campos Vacios");
            
        }else {
            
            try {
                String o = this.orden.getText();
                int con= this.nuevaOrden.consultarIdOrden(o);
                if(con==1)
                    this.mensaje.setText("La Orden ya Existe");
                else{
                    this.registarOrden();
                    this.mensaje.setText("Orden Generada Correctamente");
                    this.orden.setText("");
                    this.ciudad.setText("");
                    this.pedido.setText(fe);
                    this.entrge.setText(fe);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Generar_Orden.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_generarActionPerformed

