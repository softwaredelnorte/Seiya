    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        // TODO add your handling code here:
        if(this.idCadOrden.equals("")){
            this.mensaje.setText("Seleccione una Orden");

        }else {

            try {
                String ciudad = this.ciudad.getText();
                String fechaPed = this.pedido.getText();
                String fechaEnt = this.entrge.getText();
               
                boolean con= this.nuevaOrden.actualizarOrden(idCadOrden, idCliente, ciudad, fechaPed, fechaEnt);
                if(con==false)
                this.mensaje.setText("La Orden No se pudo actualizar");
                else{
                    //this.registarOrden();
                    this.mensaje.setText("Orden Actualizada Correctamente");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Generar_Orden.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_ActualizarActionPerformed

